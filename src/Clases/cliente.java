package Clases;

public class cliente {
    private int idClie;
    private String nombreClie;
    private String apellidopClie;
    private String apellidomClie;
    private String calleClie;
    private String colClie;
    private String cpClie;

    public cliente() {}

    public int getIdClie() {
        return idClie;
    }
    public void setIdClie(int idClie) {
        this.idClie = idClie;
    }
    public String getNombreClie() {
        return nombreClie;
    }
    public void setNombreClie(String nombreClie) {
        this.nombreClie = nombreClie;
    }
    public String getApellidopClie() {
        return apellidopClie;
    }
    public void setApellidopClie(String apellidopClie) {
        this.apellidopClie = apellidopClie;
    }
    public String getApellidomClie() {
        return apellidomClie;
    }
    public void setApellidomClie(String apellidomClie) {
        this.apellidomClie = apellidomClie;
    }
    public String getCalleClie() {
        return calleClie;
    }
    public void setCalleClie(String calleClie) {
        this.calleClie = calleClie;
    }
    public String getColClie() {
        return colClie;
    }
    public void setColClie(String colClie) {
        this.colClie = colClie;
    }
    public String getCpClie() {
        return cpClie;
    }
    public void setCpClie(String cpClie) {
        this.cpClie = cpClie;
    }
}
