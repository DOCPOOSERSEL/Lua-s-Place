package Clases;

import java.util.Date;

public class empleado {
    private int idEmp;
    private String nombreEmp;
    private String apellidopEmp;
    private String apellidomEmp;
    private Date fechaContrato;
    private Date fechaNacEmp;
    private String cpEmp;
    private String rfcEmp;
    private String calleEmp;
    private String colEmp;

    public empleado() {}

    public int getIdEmp() {
        return idEmp;
    }

    public void setIdEmp(int idEmp) {
        this.idEmp = idEmp;
    }

    public String getNombreEmp() {
        return nombreEmp;
    }

    public void setNombreEmp(String nombreEmp) {
        this.nombreEmp = nombreEmp;
    }

    public String getApellidopEmp() {
        return apellidopEmp;
    }

    public void setApellidopEmp(String apellidopEmp) {
        this.apellidopEmp = apellidopEmp;
    }

    public String getApellidomEmp() {
        return apellidomEmp;
    }

    public void setApellidomEmp(String apellidomEmp) {
        this.apellidomEmp = apellidomEmp;
    }

    public Date getFechaContrato() {
        return fechaContrato;
    }

    public void setFechaContrato(Date fechaContrato) {
        this.fechaContrato = fechaContrato;
    }

    public Date getFechaNacEmp() {
        return fechaNacEmp;
    }

    public void setFechaNacEmp(Date fechaNacEmp) {
        this.fechaNacEmp = fechaNacEmp;
    }

    public String getCpEmp() {
        return cpEmp;
    }

    public void setCpEmp(String cpEmp) {
        this.cpEmp = cpEmp;
    }

    public String getRfcEmp() {
        return rfcEmp;
    }

    public void setRfcEmp(String rfcEmp) {
        this.rfcEmp = rfcEmp;
    }

    public String getCalleEmp() {
        return calleEmp;
    }

    public void setCalleEmp(String calleEmp) {
        this.calleEmp = calleEmp;
    }

    public String getColEmp() {
        return colEmp;
    }

    public void setColEmp(String colEmp) {
        this.colEmp = colEmp;
    }
}
